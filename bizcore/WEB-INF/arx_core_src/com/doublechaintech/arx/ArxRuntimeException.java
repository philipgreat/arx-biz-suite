
package com.doublechaintech.arx;


public class ArxRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public ArxRuntimeException() {
        super();
    }


    public ArxRuntimeException(String message) {
        super(message);
    }


    public ArxRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public ArxRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












